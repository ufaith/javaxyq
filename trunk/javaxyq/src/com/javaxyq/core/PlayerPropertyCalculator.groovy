class PlayerPropertyCalculator {
	
	private static def 人族门派 = [ '大唐官府', '方寸山' ,'化生寺', '女儿村'];
	private static def 魔族门派 = [  '阴曹地府', '魔王寨', '狮驼岭', '盘丝洞 '];
	private static def 仙族门派 = ['天宫' ,'龙宫', '五庄观', '普陀山'];

	private static def 人族 = ['0001','0002','0003','0004'];
	private static def 魔族 = ['0005','0006','0007','0008'];
	private static def 仙族 = ['0000','0010','0011','0012'];
	                            
	static int calc速度(PlayerVO attrs) {
		return attrs.体质*0.1 + attrs.耐力*0.1 + attrs.力量*0.1 + attrs.敏捷*0.7 + attrs.魔力*0;
	}

	static int calc灵力(PlayerVO attrs) {
		return attrs.体质*0.3 + attrs.魔力*0.7 + attrs.耐力*0.2 + attrs.力量*0.4 + attrs.敏捷*0
	}

	static int calc躲避(PlayerVO attrs) {
		//FIXME 躲避计算公式？
		return attrs.敏捷*1 + 10;
	}
	static int calc体力(PlayerVO attrs) {
		return attrs.level*10 + 50;
	}
	static int calc活力(PlayerVO attrs) {
		return attrs.level*10 + 50;
	}

	static int calc命中(PlayerVO attrs) {
		switch(attrs.character) {
			case 人族:
				return attrs.力量*2+30
			case 魔族:
				return attrs.力量*2.3+27;
			case 仙族:
				return attrs.力量*1.7+30;
		}
	}

	static int calc伤害(PlayerVO attrs) {
		switch(attrs.character) {
			case 人族:
				return attrs.力量*0.7+34 
			case 魔族:
				return attrs.力量*0.8+34 
			case 仙族:
				return attrs.力量*0.6+40 
		}
		
	}

	static int calc防御(PlayerVO attrs) {
		switch(attrs.character) {
			case 人族:
				return attrs.耐力*1.5 
			case 魔族:
				return attrs.耐力*1.3 
			case 仙族:
				return attrs.耐力*1.6 
		}
	}

	static int calc气血(PlayerVO attrs) {
		switch(attrs.character) {
			case 人族:
				return attrs.体质*5 + 100 
			case 魔族:
				return attrs.体质*6 + 100 
			case 仙族:
				return attrs.体质*4.5 + 100 
		}
	}
	
	static int calc魔法(PlayerVO attrs) {
		switch(attrs.character) {
			case 人族:
				return attrs.魔力*3+80 
			case 魔族:
				return attrs.魔力*2.5+80 
			case 仙族:
				return attrs.魔力*3.5+80 
		}
	}
}

